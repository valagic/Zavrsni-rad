    @Override
    public void postavke() {
        setTitle(Aplikacija.getNaslov("Trener"));
    }

    @Override
    public void ucitaj() {
        DefaultListModel<Trener> m = new DefaultListModel<>();
        obrada.read().forEach(s->{m.addElement(s);});
        lstEntiteti.setModel(m);
    }
