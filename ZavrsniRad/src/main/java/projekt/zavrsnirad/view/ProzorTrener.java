    @Override
    public void postaviVrijednostiUEntitet() {
       var e = obrada.getEntitet();
       e.setIme(txtIme.getText());
       e.setPrezime(txtPrezime.getText());
    }

    private void pocistiPodatke() {
        txtIme.setText("");
        txtPrezime.setText("");
    }
